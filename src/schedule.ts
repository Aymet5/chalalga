export function formatScheduleMessage(): string {
  const daily = dailyKhurals.map((item) => `• ${item.time} — ${item.title} (${item.purpose})`).join('\n');
  const special = marchMainKhurals
    .map((item) => `• ${item.dateLabel}: ${item.title} (${item.purpose})`)
    .join('\n');

  return [
    '🗓 Расписание хуралов, Иволгинский дацан «Хамбын Хурээ» (март 2026)',
    '',
    'Ежедневные хуралы:',
    daily,
    '',
    'Основной хурал 15:00 (вторая половина марта):',
    special
  ].join('\n');
}
